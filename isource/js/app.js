const square = document.querySelector('.discription');
square.classList.remove('w3-animate-top');

// Create the observer, same as before:
const observer = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      square.classList.add('w3-animate-top');
      return;
    }

  });
});
observer.observe(document.querySelector('.discription'));
